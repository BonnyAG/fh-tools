			<h3 class="mt-2 text-lg font-bold text-gray-900">How does the Citation Builder work?</h3>
			<p class="text-gray-600">To use the citation builder, fill each of the relevant fields. As you do so, you will see the citation update in the grey box underneath the citation builder. Once you're done with creating your citation, click on the grey box that contains that citation and it will be copied to your clipboard.</p>
			<h3 class="mt-2 text-lg font-bold text-gray-900">Can I see a history of the citations I create?</h3>
			<p class="text-gray-600">No. This site doesn't store any data to avoid having to deal with databases and servers, and in order to keep this site free and maintainable. Therefore, I have no way of keeping track of the citations you create. All data that you may enter on the site is cleared when you leave the site.</p>
